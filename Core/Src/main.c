void getRealTemperature()
{
	HAL_ADC_Start(&hadc1);
	if(HAL_ADC_PollForConversion(&hadc1, 5) == HAL_OK)
	{
		uint32_t adcVal = HAL_ADC_GetValue(&hadc1);
		float tempFahrenheit = (adcVal/4096.0)*3.3*100; // Fahrenheit
		realTemp = (tempFahrenheit - 32) * 0.555; // celcius

	}
	HAL_ADC_Stop(&hadc1);
}

